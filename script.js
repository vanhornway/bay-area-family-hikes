function build(){

const app=document.getElementById("app");

Object.keys(hikes).forEach(season=>{

const section=document.createElement("div");
section.className="season "+season;
section.innerHTML=`<h2>${season.toUpperCase()}</h2>`;

hikes[season].forEach(h=>{

let food="";
let acts="";

h.food.forEach(f=>food+=`<a href="${f[1]}" target="_blank">${f[0]}</a><br><br>`);
h.activities.forEach(a=>acts+=`<a href="${a[1]}" target="_blank">${a[0]}</a><br><br>`);

section.innerHTML+=`

<div class="card">

<img src="${h.thumbnail}" style="width:100%;border-radius:10px;margin-bottom:10px;">

<h3>${h.name}</h3>

<p>${h.distance} • ${h.elevation}</p>

<div class="links">

<a href="${h.alltrails}" target="_blank">AllTrails</a><br><br>
<a href="${h.map}" target="_blank">Google Maps (Trailhead)</a>

</div>

<div class="details">

<b>Food</b><br><br>
${food}

<b>Activities</b><br><br>
${acts}

</div>

</div>

`;

});

app.appendChild(section);

});

}

build();
